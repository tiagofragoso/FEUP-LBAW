    public function likeComment($id){
       
        if (!Auth::check()) return response(403);
        
        if (is_null(Comment::find($id))) return response(404);
        Auth::user()->likeComment($id);
        return response(200);
        
    }
    public function dislikeComment($id){
        
        if (!Auth::check()) return response(403);
        if (is_null(Comment::find($id))) return response(404);
        Auth::user()->dislikeComment($id);
        return response(200);

    }
